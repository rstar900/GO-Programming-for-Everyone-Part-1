// Program to draw a sailboat with a sail, mast and a hull
// using just fmt.Println()

package main

import "fmt"

func main() {
	// Sail
	fmt.Println("    /\\")
	fmt.Println("   /  \\")
	fmt.Println("  /    \\")
	fmt.Println(" /      \\")
	fmt.Println("/        \\")
	fmt.Println("---||-----")
	// Mast
	fmt.Println("___||_________")
	// Hull
	fmt.Println("\\____________/")
}
